public class MyOperator3
{
	public static void main(String[] args) 
	{
		System.out.println("5. 조건 연산자(삼항 연산자)----");
		/*
		변수 선언문 = (조건식)? 값1: 값2;
		조건식이 true이면 값1을 변수에 대입하고,
			   false면 값2를 변수에 대입한다.
		   이때 주의. 변수와 값1, 값2는 같은 자료형이어야한다.
		*/
	int a=50;
	int b=60;
	int c=(a>b)? a:b;
	System.out.println("c="+c);

	/*
	String str=(a<b)? a:b;
	System.out.println("str="+str);
	↑ String은 int랑 안맞고 +""  을 붙여주면 문자화가 됨
	*/
	String str=(a<b)? a+"":b+"";
	System.out.println("str="+str);

	}
}
